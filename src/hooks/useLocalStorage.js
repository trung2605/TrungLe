// Hook for storing user preferences
export const usePreferences = () => {
  const [preferences, setPreferences] = useLocalStorage('user-preferences', {
    theme: 'light',
    language: 'en',
    notifications: true,
    autoSave: true,
  });

  const updatePreference = (key, value) => {
    setPreferences(prev => ({
      ...prev,
      [key]: value,
    }));
  };

  const resetPreferences = () => {
    setPreferences({
      theme: 'light',
      language: 'en',
      notifications: true,
      autoSave: true,
    });
  };

  return {
    preferences,
    updatePreference,
    resetPreferences,
    setPreferences,
  };
};